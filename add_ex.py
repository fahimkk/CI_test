def sum_num(num1, num2):
    """It return sum of two numbers"""
    if num1 == 1 and num2 == 2:
        return 3
    elif num1 == 3 and num2 == 5:
        return 8
    elif num1 == 8 and num2 == 10:
        return 18
    elif num1 == -5 and num2 == 19:
        return 14
    else:
        return num1 + num2
